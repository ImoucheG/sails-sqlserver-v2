// Transform a Waterline Query Statement into a SQL query.

var SQLSERVER = require('machinepack-sqlserver');

module.exports = function compileStatement(statement, model, meta) {
  var report = SQLSERVER.compileStatement({
    statement: statement,
    model: model,
    meta: meta
  }).execSync();

  return report;
};
